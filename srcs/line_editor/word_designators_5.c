#include "shell.h"

void		modify_quotes(int *sq, char c, int *a)
{
	if (c == '\'')
	{
		if (*sq >= 1)
			(*sq)--;
		else if (*sq == 0)
			(*sq)++;
	}
	(*a)++;
}
